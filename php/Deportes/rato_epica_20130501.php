  <img src="../img/rato_epica_20130501.jpg" >
  <p>El fútbol puede ser apoteósico. Casi lo fue ayer para Chamartín en una noche vibrante, pero desoladora para un Madrid que tuvo épica al principio y al final. La hora restante fue del Dortmund. Con la grada encendida y la eliminatoria cardiaca, el Madrid murió con la heroica, con su vieja genética y con el Bernabéu como dicta su historia, como un teatro de los horrores para sus visitantes. Al abrigo de ese mágico romance de este escenario con la Copa de Europa, el equipo español encontró el sueño con Benzema al frente, señalado como su futbolista menos sanguíneo, y con Sergio Ramos, un coloso. Toda una secuela del linaje de los Pirri, Camacho y compañía. Talento y coraje, identidades blancas de toda la vida.<br><br>

Al Madrid, que debe reprocharse su atrofia en Alemania, no le alcanzó para la epopeya. Tampoco con José Mourinho, un lector de resultados que llegó al club con la Champions como objetivo indiscutible. Hoy, como mucho, dejará la institución con una Liga y quizá dos Copas, poco botín para su celebridad y su egómetro. Por tercer curso consecutivo, el Madrid se extravió en semifinales, lo que no puede ser el listón para un club que ya ha disputado 12 finales de Copa de Europa. Para una entidad que despidió de mala manera a los dos técnicos que ganaron sus tres últimas Champions: Heynckes y Del Bosque. No vale con ser cabeza de serie, a lo que quiso agarrarse Mourinho. Lo cierto es que su equipo recurrió a lo homérico porque tres temporadas después cuesta concretar su idea futbolística. Habrá que ver ahora si Florentino Pérez cambia el registro con un nuevo salto con pértiga.<br><br>
El Borussia siguió la estela del Barça y el Bayern, aunque estuvo a un paso del descalabro, desvalido de entrada y sometido a una última descarga brutal. Tuvo mucho gobierno y un notable catálogo de ocasiones en el segundo acto, pero Lewandowski no fue el mismo, se le desvió el punto de mira y dio vidilla a un enemigo al que ha superado en toda la competición. Quizá porque su técnico, el club en general, concibe este juego como un objeto de pasión, no de puro consumo. Le falta chequera, pero tiene olfato para bucear en el segundo escalón del mercado. Si algo le falta es experiencia, y en Madrid casi lo paga al subir y bajar la persiana.<br><br>

No hubo tanteo inicial. El Madrid se lanzó desde La Castellana, oleada tras oleada. Los mejores presagios para el equipo español, que arrancó como esos boxeadores que no tienen miramientos desde el primer asalto. Sobrecogedor para el Borussia, sonado y a punto de irse a la lona frente a un regimiento desbocado. Higuaín, Cristiano en dos ocasiones y Özil se quedaron a un dedo del gol. Si los tantos son hijos del juego, bien que los mereció el Madrid en ese primer cuarto de hora.<br><br>

Sin tregua, con la presión muy alta y una circulación de la pelota tan vertiginosa como precisa. Nada que ver con el Madrid que levitó en la ida. Mejor reseteado con Ramos por Pepe como central y Di María por Khedira. En Chamartín, atizada la hoguera ambiental, esta vez apareció un grupo de centuriones. Ramos, por las bravas o por lo civil, no daba aliento a Lewandowski, Modric hacía de catalizador, Özil descorchaba el fútbol cerca del área alemán y Di María y Cristiano —mermado físicamente— picaban por las orillas, donde más sufre el Dortmund. Acogotados, a los chicos de Klopp les entró un ataque de pánico, máxime tras la lesión de Götze en plena manada madridista. Como si la historia del Madrid se le hubiera caído encima, con el reloj de reojo desde muy pronto. A un soplo del desplome rival, el Madrid se vio incapaz de sostener el trepidante comienzo.
 Lejos de tramitar un respiro, el Madrid se tornó confuso en la misma medida que se agigantó su rival, ya liberado de los espíritus escénicos. A hombros de Gündogan, un excelente mediocentro, resistente y geométrico, y de Reus, que ocupó el sitio de Götze, el Borussia poco a poco anestesió al conjunto local. El Madrid perdió expresividad y mostró su versión más rasa. Al revés que al principio, reculó más, se olvidó de las orillas y se empecinó en enredarse en el embudo central del Dortmund, mucho más cómodo en esa habitación cerrada que genera por el eje central para encapsular al contrario. Bascular le cuesta más, mucho más. Cegado el Madrid, Di María, que había partido como un avión, se descontroló, llegó el alboroto, los pelotazos kilométricos. Por esa vía, el equipo blanco se fue alejando de Wembley. El sueño pareció esfumarse del todo cuando el Borussia se puso su frac habitual en el segundo tiempo. Lewandowski, por dos veces, una de ellas con un remate explosivo al larguero, y Gündogan —al que respondió Diego López con un paradón— hicieron tiritar al Bernabéu. El Dortmund se desplegaba con el vigor que le ha caracterizado durante toda la competición, en la que ayer perdió por primera vez.<br><br>
 Mourinho agitó el curso del juego con la entrada de Benzema y Kaká y un nuevo molde defensivo, con solo tres zagueros: Essien, Varane y Ramos. El Dortmund se veía en Londres. Pero hay algo que no cambia en el Madrid, tampoco se rinde en estos tiempos. Donde no le llegaba el fútbol, tiró de agallas y logró que al Borussia le entrara el segundo mal de altura de la jornada. Ayudó Klopp, que retiró al magullado Lewandowski y el equipo se atrincheró.<br><br>

Al toque de corneta de Sergio Ramos, el Madrid encontró a Benzema y llegó la algarabía total en Chamartín, que antes del descuento estaba a un gol de la remontada. Al Borussia se le vino encima la caballería, se vio incrustado en la barricada de Weidenfeller. Pero el Madrid murió en la orilla. Esta vez, ninguna actitud que censurarse. Lo entienda o no Mourinho, el madridismo se llevó un desengaño porque no le basta con las semifinales. Por eso es un club especial. Esa era y es su gloria centenaria. Nadie es más especial que el propio Madrid. Una décima siempre será más importante que una tercera.
  </p>

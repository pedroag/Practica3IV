  <h3> <a onmouseover="window.open('../relacionadas.html','Otras noticias','width=200,height=350')">La duda no abandona al deporte español </a></h3>
  <img src="../img/mayor_caso_dopaje_españa_20130430.jpg" >
  <p> En sus alegatos finales, todas las acusaciones de la Operación Puerto recordaron a la jueza Julia Patricia Santamaría que lo que se había juzgado en Madrid entre el 28 de enero y el 2 de abril no era sino un escandaloso caso de dopaje aunque, por imperativo legal (ausencia de una ley antidopaje en el momento de los hechos, mayo de 2006), se hubiera disfrazado de delito contra la salud pública. Todos, además, advirtieron a la juzgadora, y desde ayer sentenciadora, de que los ojos del mundo estaban clavados en su pequeño juzgado penal de la periferia madrileña, y que lo que ella decidiera tendría repercusiones en el futuro deportivo y económico de su país, y hasta en la posible concesión a Madrid de los Juegos Olímpicos de 2020.<br>
    <br>
    A todos ellos respondió la jueza con 361 páginas tamaño folio y letra en pequeño cuerpo (155.204 palabras, exactamente) y una sentencia tan frustrante para todas las partes que, llámenlo efecto bumerán, consiguió el milagro de que todos también hallaran en ella elementos positivos. <br>
    <br>
    Al principal acusado, el doctor Eufemiano Fuentes, la jueza le condena a un año de prisión (que no cumplirá pues, a menos que se cuente con antecedentes, no ingresan en prisión los condenados a menos de dos años y un día) y cuatro de inhabilitación para ejercer en adelante la medicina deportiva. A Ignacio Labarta, su cómplice, según fija la sentencia, a cuatro meses de prisión y otros tantos de inhabilitación como entrenador deportivo o cualquier cargo relacionado con el ciclismo.<br>
    <br>
    Para ambos, las acusaciones solicitaban hasta dos años y medio de sanción, pero la jueza les fijó una condena dentro del tramo inferior, pues tuvo en cuenta el tiempo transcurrido desde los hechos, casi siete años, para moderar la pena. “Mas”, añade la jueza, “sin considerarse tampoco adecuado imponer la pena mínima a la vista de la gravedad de las conductas enjuiciadas. Tan solo a la vista del elevado número de muestras de sangre, plasma y concentrados de hematíes encontradas en los registros, resulta que el daño potencial para la salud de los deportistas donantes de las bolsas no puede considerarse de escasa entidad sino bastante elevado. Y más allá de los pocos ciclistas que han reconocido en el plenario haber sido objeto de extracciones o reinfusiones, consideramos que el número de potenciales clientes-víctimas de Eufemiano Fuentes podría rondar el centenar en el momento de los hechos”.<br>
    <br>
    A Yolanda Fuentes, Vicente Belda y Manolo Saiz, la jueza los absuelve, pues durante el juicio no quedó probada su participación en los hechos.<br>
    <br>
    ¿Y las bolsas, la considerada madre del cordero, el elemento que permitiría de una vez acabar con las dudas sobre los deportistas realmente implicados en la Operación Puerto, como, presuntamente, la senadora y atleta campeona del mundo Marta Domínguez?<br>
    <br>
    Las bolsas, decidió la jueza, deberán ser destruidas sin que nadie las toque una vez haya sentencia firme, es decir, una vez resueltos los probables recursos que algunas de las partes (seguramente las acusaciones y las defensas de los dos condenados) puedan interponer en un plazo de 10 días a la Audiencia Provincial (tardaría unos 10 meses en resolver) y posteriormente al Tribunal Supremo.<br>
    <br>
    A la única voz representando al Estado español que habló ayer, Ana Muñoz Merino, directora de la Agencia Estatal Antidopaje (AEA), la sentencia le dejó satisfecha a medias. Satisfecha porque por primera vez un médico es condenado por un delito contra la salud que la jueza reconoce se produjo por dopar a deportistas. Además, Muñoz valora que la jueza subraye que las transfusiones no se efectuaban para proteger la salud de los deportistas, sino para que rindieran más, y que además eran peligrosas. A medias porque no comparte la absolución de tres de los acusados ni tampoco que a Fuentes se le inhabilite únicamente para ejercer la medicina deportiva, cuando cometió un delito contra la salud pública, no contra la salud deportiva.<br>
    <br>
    “Yo no iría a su consulta”, dijo Muñoz, quien anunció que la AEA y el Consejo Superior de Deportes (CSD) reclamarían por las vías posibles la entrega de las citadas bolsas, recurrirían las partes de la sentencia con las que no están de acuerdo y solicitarían a la jueza la entrega de todos los documentos que permitieran reabrir expediente a los ciclistas inicialmente identificados y nunca sancionados. Fuentes de la investigación policial que llevó a cabo la Operación Puerto mostraron su satisfacción, sin embargo, pues la sentencia, al ser condenatoria, reconoce la existencia de delito y justifica años después la necesidad de una operación con bolsas de sangre y documentos que ningún otro país, ni Francia ni Italia, pese a contar con leyes antidopaje más severas y más antiguas, ha sido capaz de llevar a cabo.<br>
    <br>
    Pocos elementos positivos hallaron en la sentencia, sin embargo, ni los deportistas españoles más concienciados, que saturaron sus cuentas de Twitter, ni los medios extranjeros que hacen llegar a España la mirada exterior sobre sus éxitos deportivos y su lucha antidopaje —dudas, descrédito, sombras—, quienes lamentaron amargamente que la jueza decidiera no entregar a las diferentes instancias deportivas nacionales e internacionales que las habían solicitado una muestra de cada una de las más de 200 bolsas de sangre congeladas en Barcelona para someterlas a análisis de ADN e identificar definitivamente los deportistas que recurrieron al banco de sangre y el supermercado de EPO, Igf-1 y hormona de crecimiento de Fuentes para doparse.<br>
    <br>
    Todos ellos mostraron su desilusión y su incomprensión. Los motivos de la negativa de la jueza, sin embargo, ni son nuevos ni son estrambóticos, y en ellos ha tenido peso incluso, y así lo refleja la jueza, la manera en la que el Comité Olímpico Italiano, CONI, engañó al juez instructor Serrano para conseguir una muestra de la bolsa 18 y sancionar a Alejandro Valverde.<br>
    <br>
    La jueza recuerda las razones de Serrano para anular (demasiado tarde para Valverde) la entrega de la bolsa, que no son otros que los elementos de prueba conseguidos en una investigación penal violando los derechos fundamentales de los acusados (la inviolabilidad de las comunicaciones y el domicilio, entre otros) no pueden ser usados para un procedimiento administrativo. Además, recuerda la jueza, que escribe que tampoco en la justicia “todo vale”, tampoco sería lícito usar la prueba del ADN para sancionar a un deportista por dopaje.
    </p>

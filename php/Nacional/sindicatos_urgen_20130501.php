  <h3><a onmouseover="window.open('../relacionadas.html','Otras noticias','width=200,height=350')"> Los sindicatos urgen en el Primero de Mayo a un pacto frente al paro </a></h3>
  <img src="../img/sindicatos_primero_mayo_20130501.jpg">
  <p>Un dato ha estado muy presente en las manifestaciones del Primero de Mayo: 6.202.700 millones de desempleados. Ha estado en los discursos, en los carteles y en las pancartas. Este dato, junto con otros como el número de hogares con todos sus miembros en paro (1,9 millones) o el de desempleados sin prestación o subsidio (más de 3,5 millones), llevaron a UGT y CC OO a hablar de una situación de “emergencia nacional” y a exigir al Gobierno que “convoque a la sociedad española”, en palabras del líder de CC OO, Ignacio Fernández Toxo, para buscar un “gran acuerdo” para recuperar la actividad, generar empleo y cambiar el modelo económico.<br><br>

“Hay una situación de emergencia y nosotros estamos dispuestos a dar un paso adelante”, prosiguió Toxo en su discurso al final de la manifestación que se celebró en Madrid, el que rememoró el “espíritu de los Pactos de la Moncloa” firmados en 1977. El secretario general de UGT, Cándido Méndez, caminó en la misma senda, pero marcó los límites la oferta: “El acuerdo tiene que ser para cambiar a mejor las previsiones. Diremos no a la adhesión inquebrantable a los recortes”. Tanto uno como otro comenzaron sus palabras recordando a los más de 400 trabajadores fallecidos en Bangladesh en el derrumbe de una fábrica. “Este es el resultado de la globalización económica sin la globalización de derechos”, sintetizó el secretario general de CC OO.<br><br>

A pesar del mal tiempo y del puente festivo de cinco días en Madrid, varios miles de personas acudieron a la manifestación. Los sindicatos que cifraron la asistencia en 40.000 personas (la policía no facilitó datos). Antes de la manifestación, Méndez dijo esperar “una gran asistencia”. Aunque seguidamente él mismo, por si acaso había una herida, puso la venda: “Si alguien intenta entrar en una guerra de cifras se equivoca”, ya que, según dijo, no todos los que rechazan las medidas del Gobierno iban a acudir. Lo cierto es que el Primero de Mayo de este año, bajo el lema No tienen límites. Lucha por tus derechos, acabó siendo el más numeroso de la crisis con diferencia. Solo en 2012, al calor de la reforma laboral y las movilizaciones contra ella, los sindicatos han logrado convocar a más asistentes.<br><br>

“En primer lugar hay que poner el empleo”´, reclamó Méndez en la rueda de prensa anterior a la marcha. Lo que reclaman los sindicatos es “un plan de choque para impulsar la actividad económica” que parece sacado de un manual keynesiano: un plan de infraestructuras a tres años, un plan de vivienda social en alquiler, otro para impulsar la rehabilitación, el desarrollo de la ley de dependencia y una renta mínima de ciudadanía para todos aquellos parados que han perdido la prestación y el subsidio. “Es mentira que no hay alternativas”, resumió Toxo.<br><br>

Consciente de que la austeridad, ahora ligeramente atenuada al permitir la UE que España baje del 3% de déficit en 2016, es la única vía de salida a la crisis que marca Bruselas, Méndez no ahorró críticas a la Comisión Europea. “Rehn oficia de bombero pirómano”, atacó. También Toxo dirigió sus dardos contra las políticas comunitarias, pero reclamó que no se usen como excusa: “Hay margen”.<br><br>

No parece fácil que Gobierno y sindicatos encuentren un punto de encuentro. Más bien parece imposible. Para poner en marcha todas las medidas que reclaman las centrales hace falta gasto público. El Ejecutivo, en cambio, está inmerso en la senda de cotnención del déficit. Además, para financiar sus propuestas UGT y CC OO reclaman una reforma fiscal que se traduzca en subida de impuestos porque “España tiene un problema de ingresos público, no de gastos”. Mientas que el Ejecutivo se resiste a una subida fiscal tan significativa como la que reclaman los representantes de los trabajadores.<br><br>

La mejor prueba de esto fue la reacción que ha tenido mismo el partido que apoya al Gobierno, el PP. “Las propuestas de pacto en ningún caso deben significar la vuelta a políticas antiguas, que nos arruinaron, que no sirvieron para salvarnos de la crisis”, advirtió Estaban González Pons, vicesecretario general del PP, en referencia tanto a la oferta sindical como a la que lanzó el PSOE a comienzos de semana. Para Pons, la lucha contra el desempleo “no es de ningún partido ni de ningún sindicato”.<br><br>

Tanto UGT como CC OO aprovecharon el Primero de Mayo para rechazar la paciencia que el domingo pidió Rajoy a los españoles, después de que el pasado viernes el Gobierno admitiera que durante esta legislatura se seguirá destruyendo empleo y que el paro no bajará del 25%. “No podemos pedir paciencia a quien está en paro y van a perder su prestación, ni a los deshauciados”, explicó Jaime Cedrún, secretario general del CC OO en Madrid.<br><br>

Y si el Gobierno no rectifica, ¿qué? A diferencia de otros años, este miércoles no planeó sobre el Primero de Mayo la convocatoria de una nueva huelga general (sería la tercera contra el Ejecutivo). Solo Toxo lanzó una amenaza velada al final de su discurso: “Si no hay soluciones habrá movilizaciones, las que haga falta y durante el tiempo que haga falta”. Tampoco el referéndum que venían reclamando hasta ahora los sindicatos tuvo un gran protagonismo. Lo menciono el líder de UGT en Madrid, José Ricardo Martínez: “Rajoy engañó con su programa. Si no lo convoca [en referencia a la consulta], tendría que dimitir y convocar elecciones anticipadas”. A la espera de que se aclaren los puntos básicos de la próxima reforma de pensiones, el factor de sostenibilidad, los sindicatos han optado por guardar la munición de calibre más grueso.
